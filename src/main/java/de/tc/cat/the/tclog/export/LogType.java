package de.tc.cat.the.tclog.export;

/**
 * Indicates the different log types that are valid for the TCLog.
 */
public enum LogType {
    /**
     * Indicates the log type error.
     */
    Error,
    /**
     * Specifies the log type warning.
     */
    Warning,
    /**
     * Specifies the log type info.
     */
    Info,
    /**
     * Specifies the debug log type.
     */
    Debug,
    /**
     * Specifies the log type Unknow.
     */
    Unknow
}
